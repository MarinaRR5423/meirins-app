// Helper — t(lang, section, key)
export function t(lang = 'es', section, key) {
  return T[lang]?.[section]?.[key] ?? T['es']?.[section]?.[key] ?? key;
}

// Helper — get translated phase display data, merged with base PHASES object
export function getPhaseDisplay(lang, phaseKey, basePhasesData) {
  const override = T[lang]?.phaseDisplay?.[phaseKey] ?? T.es.phaseDisplay[phaseKey];
  if (!basePhasesData) return override;
  return { ...basePhasesData, ...override };
}

// Helper — translate a meal label (Desayuno → Breakfast)
export function getMealLabel(lang, spanishLabel) {
  return T[lang]?.mealLabels?.[spanishLabel] ?? spanishLabel;
}

